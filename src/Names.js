export const chooseBrother = (assign) => {
  const assignment = /initial|return|bibleStudy/.test(assign);
  const arrayNames = [];
  let counter = 0;
  const randomNoRepeats = (array) => {
    var copy = array; // array.slice(0);
    return function test() {
      if (copy.length < 1) {
        let broNames = onlyBrothersNames.slice(0);
        copyBrothersNames = broNames.filter(
          (val) => !removedBrothersNames.includes(val)
        );
        copy = copyBrothersNames;
      } // onlyBrothersNames.slice(0) array.slice(0); }
      var index = Math.floor(Math.random() * copy.length);
      var item = copy[index];
      let el;
      if (arrayNames.length > 0) {
        el = masterListBrothers[item].assignments.some(
          (a) => assign.includes(a) || "householder".includes(a)
        );
      } else {
        el = masterListBrothers[item].assignments.some((a) =>
          assign.includes(a)
        );
      }
      if (assignment && !el) {
        counter += 1;
        if (counter === copy.length) {
          let broNames = onlyBrothersNames.slice(0);
          copy = broNames.filter((val) => !removedBrothersNames.includes(val)); //onlyBrothersNames.slice(0);
        }
        return test();
      } else if (assignment && el && arrayNames.length < 1) {
        copy.splice(index, 1);
        removedBrothersNames.push(item);
        arrayNames.push(item);
        return test();
      } else if (assignment && el && arrayNames.length === 1) {
        copy.splice(index, 1);
        removedBrothersNames.push(item);
        arrayNames.push(item);
        return arrayNames;
      }
      if (!el && !assignment) {
        counter += 1;
        if (counter === copy.length) {
          let broNames = onlyBrothersNames.slice(0);
          copy = broNames.filter((val) => !removedBrothersNames.includes(val)); //onlyBrothersNames.slice(0);
        }
        return test();
      } else {
        removedBrothersNames.push(item);
        copy.splice(index, 1);
        return item;
      }
    };
  };
  const brotherName = randomNoRepeats(copyBrothersNames);
  return brotherName();
};
